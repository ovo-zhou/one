import prisma from "@/prisma";
import Nav from "../components/nav";
// import "../tailwind/tailwind.css";
import { Suspense, use } from "react";
import { getUserInfo } from "../actions/index";
import Link from "next/link";
import Image from "next/image";
import '../globals.css'

export const metadata = {
  title: "ryan",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }) {
  const pages = await prisma.post.findMany({
    where: {
      type: {
        equals: "page",
      },
    },
  });
  const menuItems = [
    {
      href: "/",
      title: "首页",
    },
    ...pages.map((item) => ({
      href: `/post/${item.id}`,
      title: item.title,
    })),
    {
      href: "/ai",
      title: "AI",
    },
  ];
  const userInfo = await getUserInfo();
  console.log(userInfo);
  return (
    <html lang="en">
      <body>
        <div className="container mx-auto bg-white">
          <div className="flex justify-between items-center  h-12 text-black">
            <Nav menuItems={menuItems}></Nav>
            <div className="flex h-12">
              {userInfo ? (
                <Link href={userInfo.isAdmin ? "/admin/post/list" : "#"}>
                  <div className="flex justify-center items-center h-12 gap-3 cursor-pointer">
                    <Image
                      src={userInfo.avatar}
                      className="w-4 h-4"
                      alt="头像"
                      width={36}
                      height={36}
                    />
                    {userInfo.name}
                  </div>
                </Link>
              ) : (
                <div className="hover:bg-slate-200 h-8 leading-8 px-3 rounded-md">
                  <Link href="/login">登录</Link>
                </div>
              )}
            </div>
          </div>

          <div className="relative" style={{ minHeight: "calc(100vh - 6rem)" }}>
            <Suspense fallback={<div>Loading...</div>}>{children}</Suspense>
          </div>
          <div className="text-center h-12">
            Copyright 2019-{new Date().getFullYear()} ryan 版权所有 保留所有权利
          </div>
        </div>
      </body>
    </html>
  );
}
